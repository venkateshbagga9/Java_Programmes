//Assignment operator
class Assignment
  {
    public static void main(String[]args)
    {
      int b=9;
      int v=b;
      System.out.println(v);
      v=v*3;
      System.out.println(v);
        //Short hand operator
      int y=4;
      System.out.println(y);
      y+=2;
      System.out.println(y);
      y*=5;
      System.out.println(y);
      
      
    }
  }