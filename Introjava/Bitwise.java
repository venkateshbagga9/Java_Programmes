class Bitwise
  {
    public static void main(String[]args)
    {
      int a=7,b=9;
      System.out.println("Bitwise & is "+(a&b));
       System.out.println("Bitwise | is "+(a|b));
       System.out.println("Bitwise ^ is "+(a^b));
       System.out.println("Bitwise ~ is "+(~b));
      System.out.println("Bitwise ~ is "+(~a));
      System.out.println("leftshift op 9<<7 is "+(9<<7));
       System.out.println("rightshif op 19>>7 is "+(19>>7));
      System.out.println("left shift op 40<<19 is "+(40<<19));
       System.out.println("right shift op 90>>19 is "+(90>>19));
      
    }
  }